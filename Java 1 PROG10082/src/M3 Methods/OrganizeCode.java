public class OrganizeCode {

     public static void main (String[] args) { 
		
		System.out.println("Let's Organize Your Time With Methods!");
	
		morning(); 
		afternoon(); 
		evening(); 
		night(); 
	}
	 
	public static void morning() { 
	
		System.out.println("Wear a nice dress"); 
		System.out.println("Wear a nice perfume as well!"); 
		System.out.println("Leave the house, get to work"); 
	
	} 
	
	public static void afternoon() { 
	
		System.out.println("Work, Work, Work");
		System.out.println("Go get a chicken sandwich");
		System.out.println("Go get some coffee!");
	} 
	
	public static void evening() { 
	
		System.out.println("Get home");
		System.out.println("Make a plan for a trip");
		System.out.println("Do yoga");
		System.out.println("Take a shower");

	} 
	
	public static void night() { 
	
		System.out.println("Sleep tight! üåù"); 

	} 

     
}
